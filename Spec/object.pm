    $exe_short_name =~ s/_[rs]$//;        # chop off trailing _r or _s
    push @rc, {
        'command' => $exe_full_name,
        'args'    => [ $me->size() . ".prm" ],
        'output'  => "$exe_short_name.out",
        'error'   => "$exe_short_name.err",
    };
return @rc;
}


